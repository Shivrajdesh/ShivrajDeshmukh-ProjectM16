package imp_programmes;

public class ASCII {
	
	char aa='c';
	char bb='v';
	int d=aa;
	int e=bb;     // in the ASCII program 1) 'a' is used of 97 and 'z' used as 122.
	                                    //2) 'A' is used of 65 and 'Z' used as 90.
	
	public static void main(String[] args) {
		
      ASCII z=new ASCII();
      
      System.out.println(z.d);
      System.out.println(z.e);
		
	}

}
